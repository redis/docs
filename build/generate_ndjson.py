#!/usr/bin/env python3
"""
Generate NDJSON feed from per-page JSON files.

This script concatenates all index.json files generated by Hugo into a single
docs.ndjson file (Newline Delimited JSON) for AI/LLM consumption.

Usage:
    python build/generate_ndjson.py [--public-dir PUBLIC_DIR] [--output OUTPUT]
    
Example:
    python build/generate_ndjson.py --public-dir public --output public/docs.ndjson
"""

import argparse
import json
import os
import sys
from pathlib import Path


def find_json_files(public_dir: Path) -> list[Path]:
    """Find all index.json files in the public directory."""
    json_files = []
    for json_file in public_dir.rglob("index.json"):
        # Skip any JSON files that aren't our AI output format
        # (e.g., data files, search indexes, etc.)
        try:
            with open(json_file, 'r', encoding='utf-8') as f:
                data = json.load(f)
                # Check for our expected fields
                if all(key in data for key in ['id', 'title', 'url']):
                    json_files.append(json_file)
        except (json.JSONDecodeError, KeyError):
            # Not a valid JSON file or not our format, skip it
            continue
    return sorted(json_files)


def generate_ndjson(json_files: list[Path], output_path: Path) -> int:
    """
    Concatenate JSON files into NDJSON format.
    
    Returns the number of pages processed.
    """
    count = 0
    with open(output_path, 'w', encoding='utf-8') as out:
        for json_file in json_files:
            try:
                with open(json_file, 'r', encoding='utf-8') as f:
                    data = json.load(f)
                # Write as single line (NDJSON format)
                out.write(json.dumps(data, ensure_ascii=False, separators=(',', ':')))
                out.write('\n')
                count += 1
            except (json.JSONDecodeError, IOError) as e:
                print(f"Warning: Skipping {json_file}: {e}", file=sys.stderr)
    return count


def main():
    parser = argparse.ArgumentParser(
        description='Generate NDJSON feed from per-page JSON files.'
    )
    parser.add_argument(
        '--public-dir',
        type=Path,
        default=Path('public'),
        help='Path to Hugo public directory (default: public)'
    )
    parser.add_argument(
        '--output',
        type=Path,
        default=None,
        help='Output NDJSON file path (default: <public-dir>/docs.ndjson)'
    )
    args = parser.parse_args()
    
    public_dir = args.public_dir
    output_path = args.output or (public_dir / 'docs.ndjson')
    
    if not public_dir.exists():
        print(f"Error: Public directory '{public_dir}' does not exist.", file=sys.stderr)
        print("Run 'hugo' first to generate the site.", file=sys.stderr)
        sys.exit(1)
    
    print(f"Scanning {public_dir} for index.json files...")
    json_files = find_json_files(public_dir)
    
    if not json_files:
        print("No index.json files found. Make sure Hugo generated JSON output.")
        sys.exit(1)
    
    print(f"Found {len(json_files)} JSON files.")
    print(f"Generating {output_path}...")
    
    count = generate_ndjson(json_files, output_path)
    
    print(f"Done! Wrote {count} pages to {output_path}")
    print(f"File size: {output_path.stat().st_size / 1024 / 1024:.2f} MB")


if __name__ == '__main__':
    main()

